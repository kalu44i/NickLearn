import java.util.Date;


public class Person {
	private String name;
	private String surname;
	private int age;
	private String citizenShip;
	private String gender;
	private Date birthday;
	private String maritalStatus;
	private int height;
	private int weight;
	
	String getName() {
		return name;
	}
	
	String getSurname() {
		return surname;
	}
	
	int getAge() {
		return age;
	}
	
	String getCitizenShip() {
		return citizenShip;
	}
	
	String getGender() {
		return gender;
	}
	
	Date getBirthday() {
		return birthday;
	}
	
	String getMaritalStatus() {
		return maritalStatus;
	}
	
	int getHeight() {
		return height;
	}
	
	int hetWeight() {
		return weight;
	}
	
}
